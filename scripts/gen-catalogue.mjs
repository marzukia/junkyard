#!/usr/bin/env node
// Reads apps/*/junkyard.json, validates, sorts by order, and emits
// hub/src/catalogue.generated.ts. Run before every hub build.

import { readFileSync, writeFileSync, readdirSync, statSync } from "node:fs";
import { join, dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const APPS_DIR = join(ROOT, "apps");
const OUT_FILE = join(ROOT, "hub", "src", "catalogue.generated.ts");

const VALID_CATEGORIES = new Set(["image", "text", "ai", "docs"]);

// Collect all app directory names
const appDirs = readdirSync(APPS_DIR).filter((name) => {
  try {
    return statSync(join(APPS_DIR, name)).isDirectory();
  } catch {
    return false;
  }
});

// Read and validate each junkyard.json
const tools = [];
const errors = [];

for (const dir of appDirs) {
  const jsonPath = join(APPS_DIR, dir, "junkyard.json");
  let raw;
  try {
    raw = readFileSync(jsonPath, "utf-8");
  } catch {
    errors.push(`Missing junkyard.json for app directory: ${dir}`);
    continue;
  }

  let data;
  try {
    data = JSON.parse(raw);
  } catch (e) {
    errors.push(`Invalid JSON in ${jsonPath}: ${e.message}`);
    continue;
  }

  // Required fields check
  const required = ["slug", "name", "category", "order", "tagline", "description", "incumbent", "path", "runtime", "mcp"];
  for (const field of required) {
    if (data[field] === undefined || data[field] === null) {
      errors.push(`${jsonPath}: missing required field "${field}"`);
    }
  }

  if (errors.length > 0) continue;

  // Category validation
  if (!VALID_CATEGORIES.has(data.category)) {
    errors.push(`${jsonPath}: category "${data.category}" not in [image, text, ai, docs]`);
  }

  // Slug must match directory name
  if (data.slug !== dir) {
    errors.push(`${jsonPath}: slug "${data.slug}" does not match directory name "${dir}"`);
  }

  // Order must be a positive integer
  if (!Number.isInteger(data.order) || data.order < 1) {
    errors.push(`${jsonPath}: order must be a positive integer, got ${JSON.stringify(data.order)}`);
  }

  if (errors.length === 0) {
    tools.push(data);
  }
}

if (errors.length > 0) {
  for (const err of errors) {
    process.stderr.write(`ERROR: ${err}\n`);
  }
  process.exit(1);
}

// Count of json files must equal count of app dirs (catches missing jsons)
if (tools.length !== appDirs.length) {
  process.stderr.write(
    `ERROR: found ${tools.length} valid junkyard.json files but ${appDirs.length} app directories - a file is missing or invalid\n`,
  );
  process.exit(1);
}

// Order values must be unique
const orderSet = new Set(tools.map((t) => t.order));
if (orderSet.size !== tools.length) {
  const seen = new Set();
  const dupes = [];
  for (const t of tools) {
    if (seen.has(t.order)) dupes.push(`order ${t.order} (slug: ${t.slug})`);
    seen.add(t.order);
  }
  process.stderr.write(`ERROR: duplicate order values: ${dupes.join(", ")}\n`);
  process.exit(1);
}

// Order set must be exactly 1..N
const N = tools.length;
for (let i = 1; i <= N; i++) {
  if (!orderSet.has(i)) {
    process.stderr.write(
      `ERROR: order values must be exactly 1..${N}, but ${i} is missing\n`,
    );
    process.exit(1);
  }
}

// Sort by order ascending
tools.sort((a, b) => a.order - b.order);

// Emit catalogue.generated.ts
const lines = [
  `// AUTO-GENERATED by scripts/gen-catalogue.mjs from apps/*/junkyard.json. DO NOT EDIT.`,
  `import type { Tool } from "./tools";`,
  ``,
  `export const TOOLS: Tool[] = [`,
];

for (const t of tools) {
  lines.push(`  {`);
  lines.push(`    name: ${JSON.stringify(t.name)},`);
  lines.push(`    slug: ${JSON.stringify(t.slug)},`);
  lines.push(`    yard: ${JSON.stringify(t.category)},`);
  lines.push(`    tagline: ${JSON.stringify(t.tagline)},`);
  lines.push(`    incumbent: ${JSON.stringify(t.incumbent)},`);
  lines.push(`  },`);
}

lines.push(`];`);
lines.push(``);

writeFileSync(OUT_FILE, lines.join("\n"), "utf-8");

process.stdout.write(`generated ${tools.length} tools\n`);
